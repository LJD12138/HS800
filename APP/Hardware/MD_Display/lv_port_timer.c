//****************************************************参数初始化**************************************************//
static volatile uint32_t s_ulSysTickCount = 0U;    //裸机模式下的系统节拍计数器

/***********************************************************************************************************************
 -----函数功能    LVGL定时器初始化
 -----说明(备注)  裸机模式下复位系统节拍计数器，FreeRTOS模式无需初始化
 -----传入参数    none
 -----输出参数    none
 -----返回值      none
 ************************************************************************************************************************/
void vLV_TimerInit(void)
{
#if(!boardUSE_OS)
    s_ulSysTickCount = 0U;
#endif
}

/***********************************************************************************************************************
 -----函数功能    获取LVGL系统时钟
 -----说明(备注)  返回毫秒级时间戳，用于LVGL动画和定时器管理
                  FreeRTOS模式从系统节拍转换，裸机模式使用内部计数器
 -----传入参数    none
 -----输出参数    none
 -----返回值      当前系统时间(毫秒)
 ************************************************************************************************************************/
uint32_t ulLV_GetTickMs(void)
{
#if(boardUSE_OS)
    return (uint32_t)(xTaskGetTickCount() * portTICK_PERIOD_MS);
#else
    return s_ulSysTickCount;
#endif
}

/***********************************************************************************************************************
 -----函数功能    LVGL定时器中断回调
 -----说明(备注)  裸机模式下由定时器中断调用，递增系统节拍计数器
                  FreeRTOS模式下不需要此函数
 -----传入参数    none
 -----输出参数    none
 -----返回值      none
 ************************************************************************************************************************/
void vLV_TimerIrqCallback(void)
{
#if(!boardUSE_OS)
    s_ulSysTickCount++;
#endif
}

