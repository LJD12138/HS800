void delay_us(vu16 us)
{
    vu8 j = 0;
    while(us--)
        for(j = 0; j < 20; j++);
}

void HT_SDA_OUT(void)
{
	#if (boardIC_TYPE == boardIC_GD32F50X)
    gpio_mode_set(dispHT_SDA_PORT, GPIO_MODE_OUTPUT, GPIO_PUPD_NONE, dispHT_SDA_PIN);
    gpio_output_options_set(dispHT_SDA_PORT, GPIO_OTYPE_PP, GPIO_OSPEED_LEVEL3, dispHT_SDA_PIN);
	#else
    gpio_init( dispHT_SDA_PORT,
	           GPIO_MODE_OUT_PP,
	           GPIO_OSPEED_50MHZ,
	           dispHT_SDA_PIN);
	#endif
}

void HT_SDA_IN(void)
{
	#if (boardIC_TYPE == boardIC_GD32F50X)
    gpio_mode_set(dispHT_SDA_PORT, GPIO_MODE_INPUT, GPIO_PUPD_PULLUP, dispHT_SDA_PIN);
	#else
    gpio_init( dispHT_SDA_PORT,
	           GPIO_MODE_IPU,
	           GPIO_OSPEED_50MHZ,
	           dispHT_SDA_PIN);
	#endif
}

void HT_IIC_Start(void)
{
	HT_SDA_OUT();
	dispHT_SDA_H();
	dispHT_SCL_H();
	delay_us(5);
	dispHT_SDA_L();
	delay_us(5);
	dispHT_SCL_L();
}	  

void HT_IIC_Stop(void)
{
	HT_SDA_OUT();
	dispHT_SCL_L();
	dispHT_SDA_L();
 	delay_us(5);
	dispHT_SCL_H();
	delay_us(5);
	dispHT_SDA_H();
	delay_us(5);							   	
}

u8 HT_IIC_Wait_Ack(void)
{
	u16 ucErrTime=0;
	HT_SDA_IN();
	dispHT_SCL_H();
	delay_us(5);
	while(dispHT_READ_SDA())
	{
		ucErrTime++;
		if(ucErrTime>1000)
		{
			HT_IIC_Stop();
			return 1;
		}
	}
    dispHT_SCL_L();	   
	delay_us(5);
	return 0;
} 

void HT_IIC_Ack(void)
{
	dispHT_SCL_L();	
	HT_SDA_OUT();
	dispHT_SDA_L();
	delay_us(2);
	dispHT_SCL_H();
	delay_us(2);
	dispHT_SCL_L();	
}

void HT_IIC_NAck()
{
	dispHT_SCL_L();
	HT_SDA_OUT();
	dispHT_SDA_H();
	delay_us(2);
	dispHT_SCL_H();
	delay_us(2);
	dispHT_SCL_L();	
}					 				     

void HT_IIC_Send_Byte(u8 txd)
{                        
    u8 t=0;   
	HT_SDA_OUT(); 	
	dispHT_SCL_L();
	#if(boardUSE_OS)
	taskENTER_CRITICAL();
	#endif
    for(t=0;t<8;t++)
    {       
        if((txd&0x80)>>7) {dispHT_SDA_H();}
		else              {dispHT_SDA_L();}
        txd<<=1; 	  
		delay_us(5);
		dispHT_SCL_H();
		delay_us(5); 
		dispHT_SCL_L();	
    }
	#if(boardUSE_OS)
    taskEXIT_CRITICAL();
	#endif
}

void Ht1621Wr_Comd(unsigned char command,unsigned char ComData)
{
	HT_IIC_Start();
	HT_IIC_Send_Byte(0x7C);
	HT_IIC_Wait_Ack();
	HT_IIC_Send_Byte(command);
	HT_IIC_Wait_Ack();
	HT_IIC_Send_Byte(ComData);
	HT_IIC_Wait_Ack();
	HT_IIC_Stop();
}

void FillLcdAll1(unsigned char mFillData)
{  
	unsigned char i=0;
	for (i=0;i<25;i++)
	{
		gsDisplayBuff[i]=mFillData;
	}
} 

void FillLcdFromBuff(void)
{	
	unsigned char i=0;
	#if(boardUSE_OS)
	taskENTER_CRITICAL();
	#endif
	HT_IIC_Start();
	HT_IIC_Send_Byte(0x7C);
	HT_IIC_Wait_Ack();
	HT_IIC_Send_Byte(0x80);
	HT_IIC_Wait_Ack();
	HT_IIC_Send_Byte(0x00);
	HT_IIC_Wait_Ack();
	for (i=0;i<25;i++)
	{
		HT_IIC_Send_Byte(gsDisplayBuff[i]);
		HT_IIC_Wait_Ack();
	}
	HT_IIC_Stop();
	#if(boardUSE_OS)
	taskEXIT_CRITICAL();
	#endif
}

void HT1621DispIcon(unsigned char mSegIndex,unsigned char mComIndex)
{
	if (mSegIndex>49)
	return;	 
	if (mComIndex>3)
	return;	
	if (mSegIndex&0x01)
	{
		gsDisplayBuff[(mSegIndex>>1)] |= (1<<(mComIndex+4));
	}
	else
	{  
		gsDisplayBuff[(mSegIndex>>1)] |= (1<<(mComIndex));
	}
} 

void DisplayNum(signed short int mStartX,unsigned char mNum)
{
	unsigned char i=0,tmp=0;
	tmp = LCD_NUM_DISP_TAB[mNum];
	for (i=0;i<7;i++)
	{
		if (tmp&1)
		HT1621DispIcon(NUM_COM_SEG_TAB[mStartX*14+7+i],NUM_COM_SEG_TAB[mStartX*14+i]);
		tmp >>= 1;
	} 	
}

