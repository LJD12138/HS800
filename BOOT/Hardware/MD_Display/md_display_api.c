/***********************************************************************************************************************
-----函数功能	Out显示区显示数据
-----传入参数   num
-----作者       LJD
-----日期       2026-03-19
************************************************************************************************************************/
void Display_OutNum(s16 num)
{
	bool ack=false;
	vu16 us_pwr = abs(num);
	
	if(num==0)
	{		 
		DisplayNum(16,0);//0
	}
	else
	{
		if(num > 0)
		{
			if(us_pwr/1000)
			{
			  DisplayNum(13,us_pwr/1000);
			  ack=true;
			}
			if(us_pwr%1000/100||ack)
			{
			  DisplayNum(14,us_pwr%1000/100);
			  ack=true;
			}
			
			if(us_pwr%100/10||ack)
			{
			  DisplayNum(15,us_pwr%100/10);
			}
			
			DisplayNum(16,us_pwr%10);
		}
		else
		{
			if(us_pwr/1000)
			{
			  DisplayNum(13,us_pwr/1000);
			  ack=true;
			}
			
			if(us_pwr%1000/100||ack)
			{
				if(ack ==false)
				{
					 DisplayNum(13,10);
				}
			
			  DisplayNum(14,us_pwr%1000/100);
			  ack=true;
			}
			
			
			if(us_pwr%100/10||ack)
			{
				if(ack ==false)
				{
					 DisplayNum(14,10);
				}
				
				DisplayNum(15,us_pwr%100/10);
				ack=true;
			}
			
			if(ack ==false)
			{
				 DisplayNum(15,10);
			}
			DisplayNum(16,us_pwr%10);
		}
	}
}

