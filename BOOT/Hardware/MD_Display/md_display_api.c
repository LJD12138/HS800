/***********************************************************************************************************************
-----函数功能	Input界面
-----传入参数   power
-----作者       LJD
-----日期       2026-03-19
************************************************************************************************************************/
void Display_InPwr(u16 power)
{
	bool ack=false;
	
	if(power>9999)
	{
	   power=9999;
	}
	
	if(power==0)
	{
		DisplayNum(0,0);//0
		DisplayNum(1,0);//0
		DisplayNum(2,0);//0
		DisplayNum(3,0);//0
	}
	else
	{
	  if(power/1000)
	  {
		  DisplayNum(0,power/1000);
		  ack=true;
	  }
	  
	  if(power%1000/100||ack)
	  {
		  DisplayNum(1,power%1000/100);
		  ack=true;
	  }
	  
	  if(power%100/10||ack)
	  {
		  DisplayNum(2,power%100/10);
		  ack=true;
	  }	
	  DisplayNum(3,power%10);  
	}
}

