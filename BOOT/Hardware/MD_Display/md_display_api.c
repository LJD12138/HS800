/***********************************************************************************************************************
-----函数功能	显示时间
-----传入参数   sw:0不使用时间框，1-8格数选择，9常亮
-----传入参数   min:0-5999
-----作者       LJD
-----日期       2026-03-19
************************************************************************************************************************/
void Display_Time(u16 min)
{
	uint8_t hundred_H,ten_H,units_H;
	
	if(min>=TIM_MAX)//>999H
	{		
		DisplayNum(8,9);
		DisplayNum(9,9);
		DisplayNum(10,9);
		Display_IconTimeH(); //H
	}
	else
	{
		if(min>=60)
		{
			hundred_H = (min/60)/10;
			ten_H     = (min/60)%10;
			units_H   = (min%60)/6;	
				
			DisplayNum(8,hundred_H);
			DisplayNum(9,ten_H);
			DisplayNum(10,units_H);
			
			Display_IconTimeDot(); //.
			Display_IconTimeH(); //H
		}
		else
		{	
			ten_H     = min/10;
			units_H   = min%10;	
				 
			DisplayNum(9,ten_H);
			DisplayNum(10,units_H);
			Display_IconTimeM(); //M
		}
    }
	Display_IconClock();//时钟标识
}

/***********************************************************************************************************************
-----函数功能	时间滚动
-----传入参数   tim 0常亮时间框--注意Display_Tim, 其他延时时间框格数时间
-----作者       LJD
-----日期       2026-03-19
************************************************************************************************************************/
void Display_TimRoll(u8 tim)
{
	static uint8_t i=0;
	static uint8_t grade_dey_tim=0;
	uint8_t bat_grade=2;
	uint8_t t=0;
	
	if(tim==0)
	{
	  // 常亮时间框
	}
	else
	{
	   bat_grade=i+1;	
			
	   for(;bat_grade<21;)
	   {
		  // 显示时间框
		  bat_grade++;
		  t++;
	   }
	   
	   bat_grade=1;
	   t=20-t;
	   for(;bat_grade<t;)
	   {
		  // 显示时间框
		  bat_grade++;
	   }
	   
		grade_dey_tim++;
		if(grade_dey_tim>=tim)
		{
			grade_dey_tim=0;	   
		   i++;if(i>20)i=1;
		}
    }
}

